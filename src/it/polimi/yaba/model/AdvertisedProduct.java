package it.polimi.yaba.model;

import java.io.Serializable;

import org.slim3.datastore.Attribute;
import org.slim3.datastore.Model;
import org.slim3.datastore.ModelRef;

import com.google.appengine.api.datastore.Key;

@Model(schemaVersion = 1)
public class AdvertisedProduct implements Serializable {

    private static final long serialVersionUID = 1L;

    @Attribute(primaryKey = true)
    private Key key;

    @Attribute(version = true)
    private Long version;

    private final ModelRef<Product> productRef = new ModelRef<Product>(
        Product.class);

    private final ModelRef<Advertisement> advertiseRef =
        new ModelRef<Advertisement>(Advertisement.class);

    public Key getKey() {
        return key;
    }

    public void setKey(Key key) {
        this.key = key;
    }

    public Long getVersion() {
        return version;
    }

    public void setVersion(Long version) {
        this.version = version;
    }

    public Product getProduct() {
        return getProductRef().getModel();
    }

    public ModelRef<Product> getProductRef() {
        return productRef;
    }

    public Advertisement getAdvertise() {
        return getAdvertiseRef().getModel();
    }

    public ModelRef<Advertisement> getAdvertiseRef() {
        return advertiseRef;
    }

    @Override
    public int hashCode() {
        final int prime = 31;
        int result = 1;
        result = prime * result + ((key == null) ? 0 : key.hashCode());
        return result;
    }

    @Override
    public boolean equals(Object obj) {
        if (this == obj) {
            return true;
        }
        if (obj == null) {
            return false;
        }
        if (getClass() != obj.getClass()) {
            return false;
        }
        AdvertisedProduct other = (AdvertisedProduct) obj;
        if (key == null) {
            if (other.key != null) {
                return false;
            }
        } else if (!key.equals(other.key)) {
            return false;
        }
        return true;
    }
}
